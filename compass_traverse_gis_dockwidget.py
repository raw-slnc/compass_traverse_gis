    def _get_row_kind(self, row_index):
        self._ensure_row_kinds_size()
        if row_index < len(self._row_kinds):
            return self._row_kinds[row_index]
        return ""

    def _set_row_kind(self, row_index, kind):
        self._ensure_row_kinds_size()
