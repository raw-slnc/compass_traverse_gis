    def _active_geo_anchor_from_values(self, observations, geo_values):
        geo_column = DEFAULT_NOTEBOOK_COLUMNS.index("geo_point")
        for observation in observations:
            row_index = (observation.source_line or 1) - 1
            latitude_text, longitude_text = geo_values.get((row_index, geo_column), ("", ""))
