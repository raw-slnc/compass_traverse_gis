    def _snap_calculation_splitter(self, pos, index):
        del pos, index
        if self._adjusting_calculation_splitter:
            return
        sizes = self.calculationSplitter.sizes()
        total = sum(sizes)
        if total <= 0:
            return
        left_ratio = sizes[0] / total
        snapped_ratio = round(left_ratio / 0.1) * 0.1
        snapped_ratio = max(0.2, min(0.8, snapped_ratio))
        self._set_calculation_splitter_ratio(snapped_ratio)

    def _set_calculation_splitter_ratio(self, ratio):
        sizes = self.calculationSplitter.sizes()
        total = sum(sizes)
        if total <= 0:
            total = max(self.calculationSplitter.width(), 1000)
        ratio = max(0.2, min(0.8, ratio))
        left_size = int(round(total * ratio))
        right_size = total - left_size
        current_sizes = self.calculationSplitter.sizes()
        if current_sizes == [left_size, right_size]:
            return
        self._adjusting_calculation_splitter = True
        try:
            self.calculationSplitter.setSizes([left_size, right_size])
        finally:
            self._adjusting_calculation_splitter = False

