    def _set_row_kind_cascade(self, start_row, kind):
        """Set kind at start_row and propagate forward through auto-branch rows."""
        self._ensure_row_kinds_size()
        if kind == "route_override":
            for row_index in range(start_row, len(self._auto_row_kinds)):
                if self._auto_row_kinds[row_index] == "branch":
                    self._row_kinds[row_index] = "route_override"
                else:
                    break
        elif kind == "":
            for row_index in range(start_row, len(self._row_kinds)):
                if self._row_kinds[row_index] == "route_override":
                    self._row_kinds[row_index] = ""
                else:
                    break
        self._apply_row_kind_colors()
        self._save_current_project_state()

    def _show_observation_row_context_menu(self, pos):
        index = self.observationTable.indexAt(pos)
        if not index.isValid():
            return
        row_index = index.row()
        source_line = row_index + 1
        user_kind = self._get_row_kind(row_index)
        auto_kind = (self._auto_row_kinds[row_index]
                     if row_index < len(self._auto_row_kinds) else "")
        effective_kind = user_kind or auto_kind
        is_mismatch = source_line in self._mismatch_source_lines
        is_acknowledged = source_line in self._acknowledged_mismatch_source_lines
        has_kind_action = effective_kind in ("branch", "route_override")
        if not has_kind_action and not is_mismatch and not is_acknowledged:
            return
        menu = QtWidgets.QMenu(self)
        if has_kind_action:
            if effective_kind == "branch":
                act = menu.addAction(self.tr("Treat as Route (include)"))
                act.triggered.connect(
                    lambda: self._set_row_kind_cascade(row_index, "route_override")
                )
            else:
                act = menu.addAction(self.tr("Reset to Auto"))
                act.triggered.connect(
                    lambda: self._set_row_kind_cascade(row_index, "")
                )
        if is_mismatch or is_acknowledged:
            if has_kind_action:
                menu.addSeparator()
            if is_mismatch:
                act = menu.addAction("正常なデータとして認める")
                act.triggered.connect(
                    lambda checked=False, sl=source_line: self._acknowledge_mismatch(sl)
                )
            else:
                act = menu.addAction("不一致の承認を解除")
                act.triggered.connect(
                    lambda checked=False, sl=source_line: self._unacknowledge_mismatch(sl)
                )
        menu.exec(self.observationTable.viewport().mapToGlobal(pos))

