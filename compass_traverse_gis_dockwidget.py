    def _refresh_mismatch_state(self, observations=None):
        if self._loading_project_state:
            return []
        if observations is None:
            obs, _ = self._collect_observations_from_rows(
                self._table_snapshot(),
                source_refs=range(1, self.observationTable.rowCount() + 1),
                update_blocks=False,
            )
        else:
            obs = observations
        all_mismatches = detect_label_mismatches(obs)
        all_source_lines = {m.source_line for m in all_mismatches}
        active = [
            m for m in all_mismatches
            if m.source_line not in self._acknowledged_mismatch_source_lines
        ]
        self._mismatch_source_lines = [m.source_line for m in active]
        self._acknowledged_active_source_lines = (
            all_source_lines & self._acknowledged_mismatch_source_lines
        )
        self._apply_row_kind_colors()
        self._update_mismatch_status_label()
        return active

    def _update_mismatch_status_label(self):
        lines = self._mismatch_source_lines
        if not lines:
            self._mismatch_status_label.setText("データの不一致なし →")
            self._mismatch_status_label.setStyleSheet("color: gray;")
        else:
            lines_text = ", ".join(str(sl) for sl in lines)
            self._mismatch_status_label.setText(
                f"⚠ {len(lines)}件の不一致（行 {lines_text}）"
            )
            self._mismatch_status_label.setStyleSheet("color: orange; font-weight: bold;")

