    def _parse_float(self, text, row_index, label, notes):
        cleaned = str(text or "").strip()
        if not cleaned:
            return None
        try:
            return float(cleaned.replace(",", ""))
        except ValueError:
            notes.append(f"{row_index + 1}行: {label} が数値として解釈できません")
            return None

    def _effective_azimuth(self, raw_azimuth):
        """Apply magnetic declination to convert a compass bearing to true bearing."""
        if raw_azimuth is None:
            return None
        declination = self.magneticDeclinationSpin.value()
        return (raw_azimuth + declination) % 360.0

