    def _project_coordinate_from_geo_texts(self, latitude_text, longitude_text):
        latitude = self._parse_dms_to_decimal(latitude_text)
        longitude = self._parse_dms_to_decimal(longitude_text)

        project_crs = QgsProject.instance().crs()
        if not project_crs.isValid():
            raise ValueError("QGIS プロジェクト CRS が未設定です。")

        transformer = QgsCoordinateTransform(
            QgsCoordinateReferenceSystem("EPSG:4326"),
            project_crs,
            QgsProject.instance(),
        )
        point = transformer.transform(QgsPointXY(longitude, latitude))
        return Coordinate(point.x(), point.y())

    def _parse_dms_to_decimal(self, text):
        normalized = (
            str(text or "")
            .strip()
            .replace("′", "'")
            .replace("’", "'")
            .replace("”", '"')
            .replace("″", '"')
            .replace("˚", "°")
        )
        numbers = re.findall(r"[+-]?\d+(?:\.\d+)?", normalized)
        if len(numbers) < 3:
            raise ValueError(f"DMS を解釈できません: {text}")

        degrees = float(numbers[0])
        minutes = float(numbers[1])
        seconds = float(numbers[2])
        sign = -1.0 if degrees < 0 else 1.0
        absolute = abs(degrees) + minutes / 60.0 + seconds / 3600.0
        return sign * absolute

