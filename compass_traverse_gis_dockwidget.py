    def _format_closure_status(self, computation):
        closure = computation.latest_closure()
        if closure is None:
            return "閉合点なし"

        ratio = computation.closure_ratio()
        if ratio is None:
            ratio_text = "-"
        elif ratio == float("inf"):
            ratio_text = "完全閉合"
        else:
            ratio_text = f"1:{ratio:.0f}"

        return (
            f"{closure.reference_station} に閉合 "
            f"(ΔX={closure.error_x:.3f}, ΔY={closure.error_y:.3f}, "
            f"誤差={closure.error_distance:.3f}, 比={ratio_text})"
        )

    def _format_error_value(self, computation):
        closure = computation.latest_closure()
        if closure is None:
            return "-"
        ratio = computation.closure_ratio()
        if ratio is None:
            ratio_text = "-"
        elif ratio == float("inf"):
            ratio_text = "1/∞"
        else:
            ratio_text = f"1/{ratio:.2f}"
        distance_display = convert_distance_from_meters(
            closure.error_distance,
            self._current_unit_profile().distance_unit,
        )
        return f"{ratio_text} ({distance_display:.3f} {self.distanceUnitCombo.currentText()})"

    def _format_calculation_notes(self, notes, computation):
        note_parts = list(notes)
        perimeter = computation.corrected_perimeter()
        if perimeter is not None:
            display_perimeter = convert_distance_from_meters(
                perimeter,
                self._current_unit_profile().distance_unit,
            )
            note_parts.append(
                f"補正後周長={display_perimeter:.3f} {self.distanceUnitCombo.currentText()}"
            )
        area = computation.corrected_area()
        if area is not None:
            note_parts.append(f"補正後面積={area:.3f} m2 ({math.floor(area / 10000.0 * 100) / 100:.2f} ha)")
        if not note_parts:
            return self.tr("Calculated successfully.")
        return " / ".join(note_parts)

