    def _run_calculation_preview(self):
        observations, notes = self._collect_observations_from_table()

        mismatches = self._refresh_mismatch_state(observations)
        if mismatches:
            lines_text = "\n".join(
                f"行{m.source_line}: 測量点「{m.from_station}」← 前行の目標点「{m.previous_target}」"
                for m in mismatches
            )
            reply = QtWidgets.QMessageBox.warning(
                self,
                self.tr("Data Mismatch"),
                self.tr(
                    "Please check the following items. Continue with calculation?\n\n{}"
                ).format(lines_text),
                QtWidgets.QMessageBox.StandardButton.Yes
                | QtWidgets.QMessageBox.StandardButton.No,
                QtWidgets.QMessageBox.StandardButton.No,
            )
            if reply != QtWidgets.QMessageBox.StandardButton.Yes:
                return

        self._clear_calculation_columns()
        self._rebuild_summary_table("area")
        self._set_summary_by_key("obs_count", str(len(observations)))
        self._set_summary_by_key("start_coord", self._start_coordinate_display_text())
        self._set_summary_by_key("notes", " / ".join(notes) if notes else "")

        if not observations:
            self._clear_preview_layers()
            self.notebookHintLabel.setText(
                "計算対象の行がありません。測量点・目標点・距離・方位角を入力してください。"
            )
            return

        start_coordinate, coordinate_note = self._resolve_start_coordinate(observations)
        if start_coordinate is None:
            self._clear_preview_layers()
            self._set_summary_by_key("start_coord", self.tr("Start position not set"))
            self._set_summary_by_key("notes", coordinate_note)
            self.notebookHintLabel.setText(
                coordinate_note
                or self.tr("Calculation and layer generation stopped because the start position is not set.")
            )
            return
        if coordinate_note:
            notes.append(coordinate_note)

        units = self._current_unit_profile()
        try:
            block_computations = self._compute_blocks_chained(
                observations, start_coordinate, units
            )
        except Exception as error:
            self._clear_preview_layers()
            self._set_summary_by_key("start_coord", self.tr("Calculation Error"))
            self._set_summary_by_key("notes", str(error))
            self.notebookHintLabel.setText(self.tr("Calculation error: {}").format(error))
            return

        if not block_computations:
            self._clear_preview_layers()
            self._set_summary_by_key("notes", "計算可能なブロックがありません。")
            return

        for blk_obs, comp in block_computations:
            self._apply_leg_results(blk_obs, comp)

        # Summary: first block that has a closure, otherwise block 0
        primary_comp = block_computations[0][1]
        for _blk_obs, comp_i in block_computations:
            if comp_i.latest_closure() is not None:
                primary_comp = comp_i
                break

        mode = self._detect_summary_mode(block_computations)
        if mode != self._current_summary_mode:
            self._rebuild_summary_table(mode)

        total_area_m2 = 0.0
        total_area_ha = 0.0
        total_perimeter_m = 0.0
        has_area_block = False
        route_distance_m = 0.0
        for blk_obs, comp in block_computations:
            if blk_obs:
                blk = self._detected_blocks.get(blk_obs[0].block_id or "")
                if blk is not None and blk.kind == BlockKind.AREA:
                    a = comp.corrected_area()
                    if a is not None:
                        total_area_m2 += a
                        total_area_ha += math.floor(a / 10000.0 * 100) / 100
                        has_area_block = True
                    total_perimeter_m += comp.corrected_perimeter() or comp.total_horizontal_distance()
                elif blk is not None and blk.kind in (BlockKind.ROUTE, BlockKind.BRANCH):
                    route_distance_m += comp.total_horizontal_distance()

        if has_area_block:
            perimeter_display = convert_distance_from_meters(total_perimeter_m, units.distance_unit)
            self._set_summary_by_key(
                "area", f"{total_area_ha:.2f} ha"
            )
            self._set_summary_by_key(
                "perimeter", f"{perimeter_display:.3f} {self.distanceUnitCombo.currentText()}"
            )
        else:
            area = primary_comp.corrected_area()
            perimeter_m = primary_comp.corrected_perimeter() or primary_comp.total_horizontal_distance()
            perimeter_display = convert_distance_from_meters(perimeter_m, units.distance_unit)
            self._set_summary_by_key(
                "area", "-" if area is None else f"{math.floor(area / 10000.0 * 100) / 100:.2f} ha"
            )
            self._set_summary_by_key(
                "perimeter", f"{perimeter_display:.3f} {self.distanceUnitCombo.currentText()}"
            )
        if route_distance_m > 0:
            route_display = convert_distance_from_meters(route_distance_m, units.distance_unit)
            self._set_summary_by_key(
                "route_length", f"{route_display:.3f} {self.distanceUnitCombo.currentText()}"
            )
        self._set_summary_by_key("error", self._format_error_value(primary_comp))
        self._set_summary_by_key(
            "obs_count", str(sum(len(bo) for bo, _ in block_computations))
        )
        self._set_summary_by_key("start_coord", self._start_coordinate_display_text())
        self._set_summary_by_key(
            "notes", self._format_calculation_notes(notes, primary_comp)
        )
        self._refresh_block_name_combo()
        layer_entries = [
            {
                "project_name": self._project_record.project_name,
                "record": self._project_record,
                "computation": comp,
                "observations": blk_obs,
                "block_name": self._block_display_name(blk_obs[0].block_id if blk_obs else ""),
            }
            for blk_obs, comp in block_computations
        ]
        linked_entry, linked_error = self._linked_project_layer_entry()
        if linked_entry is not None:
            layer_entries.append(linked_entry)
        elif linked_error:
            notes.append(linked_error)
            self._set_summary_by_key(
                "notes", self._format_calculation_notes(notes, primary_comp)
            )
        self._refresh_preview_layers(layer_entries)
        self._zoom_to_preview_layers()
        self._refresh_auto_row_kinds()
        self._current_dirty = False
        self._save_current_project_state()
        self.notebookHintLabel.setText(
            self.tr("HD, dX, and dY were updated, and managed layers were generated.")
        )

    def _compute_blocks_chained(self, observations, start_coordinate, units):
        """Compute traverse per block, chaining coordinates between blocks.

        For the first block the user-entered start_coordinate is used.
        For subsequent blocks the from_station of the block's first row is
        looked up in the coordinate pool built from all previously computed
        blocks.  If the junction station is not yet in the pool (e.g. the
        block order differs from the data order) the block is skipped.

        coord_pool uses the same coordinate type (raw vs corrected) as the
        layer renderer so that junction stations stay at a single position.

        Returns list of (block_observations, TraverseComputation) in order.
        """
        from collections import OrderedDict
        block_groups = OrderedDict()
        for obs in observations:
            block_groups.setdefault(obs.block_id or "blk_0", []).append(obs)

        use_corrected = self.closureEnabledCheck.isChecked()
        coord_pool = {}  # station_key → Coordinate
        results = []

        for block_idx, (_bid, blk_obs) in enumerate(block_groups.items()):
            if not blk_obs:
                continue
            first_key = normalize_station_label(blk_obs[0].from_station)

            if block_idx == 0:
                blk_start = start_coordinate
            elif first_key in coord_pool:
                blk_start = coord_pool[first_key]
            else:
                continue  # junction not computed yet; skip

            comp = compute_traverse(
                blk_obs,
                start_coordinate=blk_start,
                units=units,
                start_station=blk_obs[0].from_station,
            )

            if first_key not in coord_pool:
                coord_pool[first_key] = blk_start
            for leg in comp.leg_results:
                tk = normalize_station_label(leg.target_station)
                if tk not in coord_pool:
                    if use_corrected and leg.corrected_target_coordinate is not None:
                        coord_pool[tk] = leg.corrected_target_coordinate
                    else:
                        coord_pool[tk] = leg.target_coordinate

            results.append((blk_obs, comp))

        return results

