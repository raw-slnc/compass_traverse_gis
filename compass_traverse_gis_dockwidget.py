    def _populate_observation_table(self, rows, mapping):
        self._station_reference_targets.clear()
        saved_geo = self.observationTable.geo_values_snapshot()
        self.observationTable.clear_geo_values()
        row_count = max(12, len(rows))
        self.observationTable.setRowCount(row_count)

        for row_index in range(row_count):
            for column_index in range(len(DEFAULT_NOTEBOOK_COLUMNS)):
                self._set_table_cell_text(row_index, column_index, "")

        geo_col = DEFAULT_NOTEBOOK_COLUMNS.index("geo_point")
        rows_with_imported_geo = set()
        for row_index, row_values in enumerate(rows):
            for table_column, field_name in enumerate(DEFAULT_NOTEBOOK_COLUMNS):
                if field_name in ("geo_point", "delta_x", "delta_y"):
                    continue
                source_index = mapping.get(field_name)
                if source_index is None or source_index >= len(row_values):
                    continue
                value = str(row_values[source_index]).strip()
                if value.lower() == "nan":
                    value = ""
                self._set_table_cell_text(row_index, table_column, value)

            latitude_index = mapping.get("latitude_dms")
            longitude_index = mapping.get("longitude_dms")
            latitude_value = ""
            longitude_value = ""
            if latitude_index is not None and latitude_index < len(row_values):
                latitude_value = str(row_values[latitude_index]).strip()
            if longitude_index is not None and longitude_index < len(row_values):
                longitude_value = str(row_values[longitude_index]).strip()
            if latitude_value or longitude_value:
                rows_with_imported_geo.add(row_index)
            self.observationTable.set_geo_value(
                row_index, geo_col, latitude_value, longitude_value,
            )

        for (saved_row, saved_col), geo_val in saved_geo.items():
            if saved_row < row_count and saved_row not in rows_with_imported_geo:
                self.observationTable.set_geo_value(saved_row, saved_col, *geo_val)

        self._rebuild_station_reference_targets_from_table()
        self._refresh_station_name_cache()

    def _handle_observation_table_item_changed(self, item):
        if (
            self._loading_project_state
            or self._updating_calculation_outputs
            or self._updating_station_references
        ):
            return
        if item.column() in (
            DEFAULT_NOTEBOOK_COLUMNS.index("delta_x"),
            DEFAULT_NOTEBOOK_COLUMNS.index("delta_y"),
        ):
            return
        needs_block_redetect = item.column() in (
            DEFAULT_NOTEBOOK_COLUMNS.index("from_station"),
            DEFAULT_NOTEBOOK_COLUMNS.index("target_station"),
            DEFAULT_NOTEBOOK_COLUMNS.index("connect_to"),
            DEFAULT_NOTEBOOK_COLUMNS.index("close_to"),
        )
        if item.column() == DEFAULT_NOTEBOOK_COLUMNS.index("from_station"):
            self._sync_station_reference_labels(item.row(), item.text())
        elif item.column() in (
            DEFAULT_NOTEBOOK_COLUMNS.index("connect_to"),
            DEFAULT_NOTEBOOK_COLUMNS.index("close_to"),
        ):
            self._sync_station_reference_target_for_cell(item.row(), item.column())
        if needs_block_redetect:
            self._refresh_auto_row_kinds()
        if item.column() == DEFAULT_NOTEBOOK_COLUMNS.index("from_station"):
            self._acknowledged_mismatch_source_lines.discard(item.row() + 1)
            self._refresh_mismatch_state()
        elif item.column() == DEFAULT_NOTEBOOK_COLUMNS.index("target_station"):
            self._acknowledged_mismatch_source_lines.discard(item.row() + 2)
            self._refresh_mismatch_state()
        self._handle_state_changed()
        self._normalize_geo_assignments()

    def _sync_station_reference_labels(self, target_row, new_station_name):
        old_station_name = self._station_name_cache.get(target_row, "")
        self._station_name_cache[target_row] = str(new_station_name or "").strip()
        old_key = normalize_station_label(old_station_name)
        new_text = str(new_station_name or "").strip()
        if not old_key or not new_text:
            return

        self._updating_station_references = True
        self.observationTable.blockSignals(True)
        try:
            for (source_row, column_index), reference_row in list(self._station_reference_targets.items()):
                if column_index not in (2, 3) or reference_row != target_row:
                    continue
                self._set_table_cell_text(source_row, column_index, new_text)
        finally:
            self.observationTable.blockSignals(False)
            self._updating_station_references = False

    def _candidate_rows_for_station_label(self, station_text, exclude_row=None):
        station_key = normalize_station_label(station_text)
        if not station_key:
            return []
        matches = []
        from_column = DEFAULT_NOTEBOOK_COLUMNS.index("from_station")
        for row_index in range(self.observationTable.rowCount()):
            if exclude_row is not None and row_index == exclude_row:
                continue
            from_station = self._table_cell_text(row_index, from_column)
            if normalize_station_label(from_station) == station_key:
                matches.append(row_index)
        return matches

    def _sync_station_reference_target_for_cell(self, row_index, column_index):
        key = (row_index, column_index)
        current_text = self._table_cell_text(row_index, column_index)
        if not current_text:
            self._station_reference_targets.pop(key, None)
            return

        candidate_rows = self._candidate_rows_for_station_label(current_text, exclude_row=row_index)
        if not candidate_rows:
            self._station_reference_targets.pop(key, None)
            return

        existing_row = self._station_reference_targets.get(key)
        if existing_row in candidate_rows:
            self._station_reference_targets[key] = existing_row
            return

        if len(candidate_rows) == 1:
            self._station_reference_targets[key] = candidate_rows[0]
            return

        self._station_reference_targets.pop(key, None)

    def _rebuild_station_reference_targets_from_table(self):
        previous_targets = dict(self._station_reference_targets)
        rebuilt = {}
        for row_index in range(self.observationTable.rowCount()):
            for column_index in (
                DEFAULT_NOTEBOOK_COLUMNS.index("connect_to"),
                DEFAULT_NOTEBOOK_COLUMNS.index("close_to"),
            ):
                current_text = self._table_cell_text(row_index, column_index)
                if not current_text:
                    continue
                candidate_rows = self._candidate_rows_for_station_label(
                    current_text,
                    exclude_row=row_index,
                )
                if len(candidate_rows) == 1:
                    rebuilt[(row_index, column_index)] = candidate_rows[0]
                    continue
                existing_row = previous_targets.get((row_index, column_index))
                if existing_row in candidate_rows:
                    rebuilt[(row_index, column_index)] = existing_row
        self._station_reference_targets = rebuilt

    def _choose_station_reference(self, row_index, column_index):
        candidates = self._measurement_point_candidates(exclude_row=row_index)
        if not candidates:
            self.notebookHintLabel.setText(
                self.tr("No station candidates are available yet for Connect To or Close To.")
            )
            return

        labels = ["-"]
        for candidate in candidates:
            labels.append(candidate["display"])

        current_target_row = self._station_reference_targets.get((row_index, column_index))
        current_index = 0
        for candidate_index, candidate in enumerate(candidates, start=1):
            if candidate["row"] == current_target_row:
                current_index = candidate_index
                break

        title = self.tr("Choose Connect To") if column_index == 2 else self.tr("Choose Close To")
        selected_label, accepted = QtWidgets.QInputDialog.getItem(
            self,
            title,
            self.tr("Station candidates"),
            labels,
            current_index,
            False,
        )
        if not accepted:
            return

        if selected_label == "-":
            self._station_reference_targets.pop((row_index, column_index), None)
            self.observationTable.blockSignals(True)
            try:
                self._set_table_cell_text(row_index, column_index, "")
            finally:
                self.observationTable.blockSignals(False)
            self._mark_current_project_dirty(
                self.tr("Connect To or Close To was changed. Run Update Layers to apply it.")
            )
            return

        selected_candidate = candidates[labels.index(selected_label) - 1]
        self._station_reference_targets[(row_index, column_index)] = selected_candidate["row"]
        self.observationTable.blockSignals(True)
        try:
            self._set_table_cell_text(row_index, column_index, selected_candidate["station"])
        finally:
            self.observationTable.blockSignals(False)
        self._normalize_geo_assignments()
        self._mark_current_project_dirty(
            self.tr("Connect To or Close To was changed. Run Update Layers to apply it.")
        )

    def _measurement_point_candidates(self, exclude_row):
        raw_candidates = []
        key_counts = {}

        for row_index in range(self.observationTable.rowCount()):
            if row_index == exclude_row:
                continue
            station_name = self._table_cell_text(row_index, 0)
            if not station_name:
                continue
            station_key = normalize_station_label(station_name)
            raw_candidates.append(
                {
                    "row": row_index,
                    "station": station_name,
                    "key": station_key,
                }
            )
            key_counts[station_key] = key_counts.get(station_key, 0) + 1

        for candidate in raw_candidates:
            if key_counts[candidate["key"]] > 1:
                candidate["display"] = f"{candidate['station']} [{candidate['row'] + 1}行]"
            else:
                candidate["display"] = candidate["station"]

        return raw_candidates

