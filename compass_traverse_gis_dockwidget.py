    def _normalize_geo_assignments(self, source_row=None):
        if self._loading_project_state or self._normalizing_geo_assignments:
            return

        geo_column = DEFAULT_NOTEBOOK_COLUMNS.index("geo_point")
        geo_values = self.observationTable.geo_values_snapshot()
        if not geo_values:
            return

        components = self._connected_row_components()
        component_by_row = {}
        for component in components:
            for row_index in component:
                component_by_row[row_index] = component

        rows_to_clear = []
        blocked_source = False
        for component in components:
            geo_rows = sorted(
                row_index
                for row_index in component
                if geo_values.get((row_index, geo_column))
                and any(geo_values.get((row_index, geo_column), ("", "")))
            )
            if len(geo_rows) <= 1:
                continue
            keeper = geo_rows[0]
            for row_index in geo_rows[1:]:
                rows_to_clear.append(row_index)
                if source_row is not None and row_index == source_row:
                    blocked_source = True

        if not rows_to_clear:
            return

        self._normalizing_geo_assignments = True
        try:
            for row_index in rows_to_clear:
                self.observationTable.set_geo_value(row_index, geo_column, "", "")
        finally:
            self._normalizing_geo_assignments = False

        if source_row is not None and blocked_source:
            self.notebookHintLabel.setText(
                "同じ連結ブロックでは、若い行番号の位置だけを保持します。後ろの行の位置は削除しました。"
            )
        else:
            self.notebookHintLabel.setText(
                "接続関係に合わせて、連結ブロック内の位置を若い行番号へ統一しました。"
            )
        self._mark_current_project_dirty(
            "位置の割当を調整しました。計算・レイヤ更新で反映してください。"
        )

    def _connected_row_components(self):
        row_count = self.observationTable.rowCount()
        adjacency = {row_index: set() for row_index in range(row_count)}

        from_station_rows = {}
        target_station_rows = {}
        for row_index in range(row_count):
            from_station = normalize_station_label(self._table_cell_text(row_index, 0))
            target_station = normalize_station_label(self._table_cell_text(row_index, 1))
            if from_station:
                from_station_rows.setdefault(from_station, []).append(row_index)
            if target_station:
                target_station_rows.setdefault(target_station, []).append(row_index)

        for row_index in range(row_count):
            target_station = normalize_station_label(self._table_cell_text(row_index, 1))
            from_station = normalize_station_label(self._table_cell_text(row_index, 0))
            if target_station and target_station in from_station_rows:
                for linked_row in from_station_rows[target_station]:
                    if linked_row != row_index:
                        adjacency[row_index].add(linked_row)
                        adjacency[linked_row].add(row_index)
            if from_station and from_station in target_station_rows:
                for linked_row in target_station_rows[from_station]:
                    if linked_row != row_index:
                        adjacency[row_index].add(linked_row)
                        adjacency[linked_row].add(row_index)

        for (source_row, column_index), target_row in self._station_reference_targets.items():
            if column_index not in (2, 3):
                continue
            if source_row == target_row:
                continue
            adjacency.setdefault(source_row, set()).add(target_row)
            adjacency.setdefault(target_row, set()).add(source_row)

        components = []
        visited = set()
        for row_index in range(row_count):
            if row_index in visited:
                continue
            stack = [row_index]
            component = set()
            while stack:
                current = stack.pop()
                if current in visited:
                    continue
                visited.add(current)
                component.add(current)
                stack.extend(adjacency.get(current, ()))
            components.append(component)
        return components

