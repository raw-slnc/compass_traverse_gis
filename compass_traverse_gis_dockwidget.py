        default_rule = QgsRuleBasedRenderer.Rule(default_symbol)
        default_rule.setIsElse(True)
        default_rule.setLabel("その他")
        root_rule.appendChild(default_rule)

        layer.setRenderer(QgsRuleBasedRenderer(root_rule))

