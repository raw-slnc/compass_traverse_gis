    def _serialized_tuple_dict(self, values):
        serialized = {}
        for key, value in (values or {}).items():
            if isinstance(key, tuple):
                key_text = ":".join(str(part) for part in key)
            else:
                key_text = str(key)
            serialized[key_text] = value
        return serialized

    def _deserialized_tuple_dict(self, values):
        deserialized = {}
        for key_text, value in (values or {}).items():
            if ":" in str(key_text):
                parts = str(key_text).split(":")
                try:
                    key = tuple(int(part) for part in parts)
                except ValueError:
                    key = tuple(parts)
            else:
                key = key_text
            if isinstance(value, list):
                value = tuple(value)
            deserialized[key] = value
        return deserialized

