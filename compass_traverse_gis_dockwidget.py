    def _build_seq_index_map(self, observations):
        """Return {station_key: seq_index} using traversal-order position per block.

        Station names are opaque labels.  seq_index is the position of each
        leg TARGET within its block (block start = 0, next target = 1, …).
        Stations that appear only as FROM are not assigned (default -1 via .get).
        """
        if not observations:
            return {}
        seq_map = {}
        # Overall start station: FROM of the very first observation → seq=0
        start_fk = normalize_station_label(observations[0].from_station)
        seq_map[start_fk] = 0
        block_counter = {}          # bid → next seq value for leg targets
        first_bid = observations[0].block_id or "blk_0"
        block_counter[first_bid] = 1  # start station consumed seq=0
        last_tgt_key = start_fk
        last_tgt_seq = 0
        for obs in observations:
            bid = obs.block_id or "blk_0"
            fk = normalize_station_label(obs.from_station)
            tk = normalize_station_label(obs.target_station)
            if bid not in block_counter:
                # New block: decide where to start the counter
                if fk == last_tgt_key and last_tgt_seq == 0:
                    # FROM was the connector bridge (seq=0 already added) →
                    # first target in this block is seq=1
                    block_counter[bid] = 1
                else:
                    # FROM is a through-point; first new target is block start → seq=0
                    block_counter[bid] = 0
            seq = block_counter[bid]
            block_counter[bid] += 1
            if tk not in seq_map:   # first occurrence wins for QGIS layer coloring
                seq_map[tk] = seq
            last_tgt_key = tk
            last_tgt_seq = seq
        return seq_map

    def _build_block_id_map(self, observations):
        """Return {station_key: block_id} from observations."""
        b_map = {}
        for obs in observations:
            fk = normalize_station_label(obs.from_station)
            tk = normalize_station_label(obs.target_station)
            bid = obs.block_id or ""
            if fk not in b_map:
                b_map[fk] = bid
            if tk not in b_map:
                b_map[tk] = bid
        return b_map

    def _line_route_type(self, leg_index, computation):
        closure = computation.latest_closure()
        if closure is not None and leg_index <= closure.leg_index:
            return "area_edge"
        return "route"

    def _build_line_layer(self, computation, block_name, observations):
        layer = QgsVectorLayer(
            f"LineString{self._project_crs_suffix()}",
            f"{block_name or 'Compass Traverse'} Lines",
            "memory",
        )
        provider = layer.dataProvider()
        provider.addAttributes(
            [
                QgsField("from_st", QVariant.String),
                QgsField("to_st", QVariant.String),
                QgsField("sd", QVariant.Double),
                QgsField("hd", QVariant.Double),
                QgsField("dx", QVariant.Double),
                QgsField("dy", QVariant.Double),
                QgsField("block_nm", QVariant.String),
                QgsField("route_tp", QVariant.String),
            ]
        )
        layer.updateFields()

        features = []
        current_from = computation.start_coordinate
        for leg_index, (leg, observation) in enumerate(zip(computation.leg_results, observations)):
            target = leg.target_coordinate
            dx = leg.delta_x
            dy = leg.delta_y
            if self.closureEnabledCheck.isChecked() and leg.corrected_target_coordinate is not None:
                target = leg.corrected_target_coordinate
                dx = leg.corrected_delta_x if leg.corrected_delta_x is not None else dx
                dy = leg.corrected_delta_y if leg.corrected_delta_y is not None else dy
            feature = QgsFeature(layer.fields())
            feature.setGeometry(
                QgsGeometry.fromPolylineXY(
                    [
                        QgsPointXY(current_from.x, current_from.y),
                        QgsPointXY(target.x, target.y),
                    ]
                )
            )
            feature.setAttributes(
                [
                    leg.from_station,
                    leg.target_station,
                    observation.slope_distance if observation else None,
                    leg.horizontal_distance,
                    dx,
                    dy,
                    block_name,
                    self._line_route_type(leg_index, computation),
                ]
            )
            features.append(feature)
            current_from = target
        provider.addFeatures(features)
        layer.updateExtents()
        return layer

    def _create_preview_line_layer(self, computation, project_name, observations, block_name, group):
        layer = self._build_line_layer(computation, block_name, observations)
        self._apply_line_layer_style(layer)
